num_input = int(input("Type in a number (from 1 to 7): "))

if num_input == 1:
    print("Monday")
elif num_input == 2:
    print("Tuesday")
elif num_input == 3:
    print("Wednesday")
elif num_input == 4:
    print("Thursday")
elif num_input == 5:
    print("Friday")
elif num_input == 6:
    print("Saturday")
elif num_input == 7:
    print("Sunday")
else:
    print("Error, out of the range")